package com.hanweb.complat.entity;

public class SystemEmail {
	
	private String host;

	private String user;

	private String pwd;

	private String subject;

	private String form;

	private String form_nickname;

	private String content;

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public String getForm() {
		return form;
	}

	public void setForm(String form) {
		this.form = form;
	}

	public String getForm_nickname() {
		return form_nickname;
	}

	public void setForm_nickname(String form_nickname) {
		this.form_nickname = form_nickname;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

}
